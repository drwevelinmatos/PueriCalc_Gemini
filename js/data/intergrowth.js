export function igKey(weeks, days) {
  return `${weeks}+${days}`;
}

export function pickIntergrowthDomain(weeks, days) {
  const totalDays = weeks * 7 + days;
  const d24 = 24 * 7;
  const d33 = 33 * 7;
  const d42_6 = 42 * 7 + 6;

  if (totalDays < d24 || totalDays > d42_6) {
    return { error: 'IG fora do intervalo INTERGROWTH (24+0 a 42+6).' };
  }

  return { domain: totalDays < d33 ? 'veryPreterm' : 'newborn' };
}

export function classifyPIGAIGGIG(weightGrams, p10Kg, p90Kg) {
  const weightKg = weightGrams / 1000;
  if (weightKg < p10Kg) return 'PIG';
  if (weightKg > p90Kg) return 'GIG';
  return 'AIG';
}
