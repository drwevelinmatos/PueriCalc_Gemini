// Algoritmo matemático para interpolar a Curva Padrão (Semana a Semana)
function gerarCurva(pontos) {
    let result = {};
    for (let i = 0; i < pontos.length - 1; i++) {
        let [w1, l1, m1, s1] = pontos[i];
        let [w2, l2, m2, s2] = pontos[i+1];
        
        for (let w = w1; w < w2; w++) {
            let t = (w - w1) / (w2 - w1);
            result['s' + w] = {
                l: l1 + t * (l2 - l1),
                m: m1 + t * (m2 - m1),
                s: s1 + t * (s2 - s1)
            };
        }
    }
    let last = pontos[pontos.length - 1];
    result['s' + last[0]] = { l: last[1], m: last[2], s: last[3] };
    return result;
}

